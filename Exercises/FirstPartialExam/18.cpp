/*
 Read from SI two integers a and b. Write a program that will print all numbers from a to b (a < b),
 such as the number created from all the digits from next to the first to the next to last digit in the original number
 (the digits that will remain if we remove the first and the last digit from the original number),
 reversed (read from right to left) is divisible with the number created as the sum of the first and the last digit.
 For single and two-digit numbers, the number created from the middle digits is 0, and these numbers are not printed.
 At the end print out the count of such numbers.

 Example: For the number 82675 the number created from the digits from the next to the first up to the next to last digit,
 reversed is 762, and the sum of the first and the last digit is (8 + 5) = 13.
 When printing out the numbers, next to each number that is satisfying the condition,
 print the equation of the condition in the following form:
 original_number -> (reversed_middle == (last_digit + first_digit) * quotient)
 for example: 291 -> (9 == (1 + 2) * 3) 84575 -> (754 == (5 + 8) * 58)

 *The test case is after the solution*
*/

#include <iostream>

using namespace std;

int main() {
    int a, b;
    cin >> a >> b;
    if (a > b) {
        int temp = a;
        a = b;
        b = temp;
    }
    int brojac = 0;
    for (int i = a; i <= b; i++) {
        if (i < 100)
            continue;
        int obraten = 0;
        int prva = i % 10, posledna = 0, delitel;
        int pom = i;
        while (pom > 0) {
            posledna = pom % 10;
            pom /= 10;
        }
        int broj = i, nuli = 1, br_cifri = -1;
        while (broj >= 10) {
            broj /= 10;
            nuli *= 10;
            br_cifri++;
        }
        int skraten = (i % nuli) / 10;
        while (skraten > 0 || br_cifri != 0) {
            obraten = obraten * 10 + skraten % 10;
            skraten /= 10;
            br_cifri--;
        }
        if (obraten == 0)
            continue;
        if (obraten % (prva + posledna) == 0) {
            delitel = obraten / (prva + posledna);
            cout << i << " -> (" << obraten << " == (" << prva << " + " << posledna << ") * " << delitel << ")" << endl;
            brojac++;
        }

    }
    cout << brojac << endl;
    return 0;
}

