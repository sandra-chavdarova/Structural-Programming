/*
 Да се напише програма во која се внесуваат четири броеви.
 Доколку збирот на првите два броја е парен број и збирот на последните два броја е непарен број, да се испечати 1
 во спротивно да се испечати 0.

 Input:
 3 5 1 2

 Result:
 1
 */

#include <iostream>

using namespace std;

int main() {
    int a, b, c, d;
    cin >> a >> b >> c >> d;
    if ((a + b) % 2 == 0 && (c + d) % 2)
        cout << 1 << endl;
    else
        cout << 0 << endl;
    return 0;
}
