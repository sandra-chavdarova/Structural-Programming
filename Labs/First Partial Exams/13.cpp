/*
 Да се напише програма во која се внесуваат три броеви.
 Да се отпечати 1 доколку средниот број е истовремено поголем од двата останати броеви, или пак е истовремено помал.
 Во спротивно да се отпечати 0.

 Input:
 10 20 15

 Result:
 1
*/

#include <iostream>

using namespace std;

int main() {
    int a, b, c;
    cin >> a >> b >> c;
    if ((b > a && b > c) || (b < a && b < c))
        cout << 1 << endl;
    else
        cout << 0 << endl;
    return 0;
}
