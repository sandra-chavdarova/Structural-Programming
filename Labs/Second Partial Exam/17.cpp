/*
 Да се напише програма која вчитува квадратна матрица.
 На почетокот се внесува бројот на редици и колони N на матрицата, а потоа и елементите на матрицата.
 Да се трансформира матрицата на тој начин што секој негативен број во неа би се заменил со збирот на неговите соседи.
 Соседи на даден број во матрица се броевите до него (гледано хоризонтално и вертикално).

 Input:
 4
 5 6 7 8
 20 -1 4 3
 50 1 2 -6
 5 -8 5 4

 Result:
 5 6 7 8
 20 31 4 3
 50 1 2 9
 5 11 5 4
*/

#include <iostream>

using namespace std;

int main() {
    int n;
    cin >> n;
    int a[n][n];
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> a[i][j];
        }
    }

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            if (a[i][j] < 0) {
                int sum = 0;
                if (i - 1 >= 0)
                    sum += a[i - 1][j];
                if (i + 1 < n)
                    sum += a[i + 1][j];
                if (j - 1 >= 0)
                    sum += a[i][j - 1];
                if (j + 1 < n)
                    sum += a[i][j + 1];
                a[i][j] = sum;
            }
        }
    }

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cout << a[i][j] << " ";
        }
        cout << endl;
    }
    return 0;
}
